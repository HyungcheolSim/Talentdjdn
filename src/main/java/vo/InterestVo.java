package vo;

public class InterestVo {

	int i_idx;
	int m_idx;
	int t_idx;
	
	SellerVo seller;
	TalentVo talent;
	
	public SellerVo getSeller() {
		return seller;
	}
	public void setSeller(SellerVo seller) {
		this.seller = seller;
	}
	public TalentVo getTalent() {
		return talent;
	}
	public void setTalent(TalentVo talent) {
		this.talent = talent;
	}
	public int getI_idx() {
		return i_idx;
	}
	public void setI_idx(int i_idx) {
		this.i_idx = i_idx;
	}
	public int getM_idx() {
		return m_idx;
	}
	public void setM_idx(int m_idx) {
		this.m_idx = m_idx;
	}
	public int getT_idx() {
		return t_idx;
	}
	public void setT_idx(int t_idx) {
		this.t_idx = t_idx;
	}
	
	

	
}
